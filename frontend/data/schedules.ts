export function getCurrentSeasonSchedule(season: 'xc' | 'track'): ScheduleSeason | undefined {
  const currentYear = new Date().getFullYear()
  return schedules.find(s => s.season === season && s.year === currentYear)
}

export function getScheduleByYear(season: 'xc' | 'track', year: number): ScheduleSeason | undefined {
  return schedules.find(s => s.season === season && s.year === year)
}

export function getUpcomingMeets(season: 'xc' | 'track', limit?: number): Meet[] {
  const today = new Date().toISOString().split('T')[0]
  const allMeets = schedules
    .filter(s => s.season === season)
    .flatMap(s => s.meets)
    .filter(m => m.date >= today)
    .sort((a, b) => a.date.localeCompare(b.date))

  return limit ? allMeets.slice(0, limit) : allMeets
}

export function getArchivedSeasons(): { season: 'xc' | 'track'; year: number }[] {
  const currentYear = new Date().getFullYear()
  return schedules
    .filter(s => s.year < currentYear)
    .map(s => ({ season: s.season, year: s.year }))
    .sort((a, b) => b.year - a.year || a.season.localeCompare(b.season))
}
